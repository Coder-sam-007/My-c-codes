#include<stdio.h>

int main()
{
int num1,num2;

    printf("\nenter a digit you want to increase it by one bit:");
    scanf("%d",&num1);
    printf("\nthe result is:%d",++num1);

    printf("\nenter a digit you want to decrease it by one bit:");
    scanf("%d",&num2);
    printf("\nthe result is:%d",--num2);

    
  
    return 0;
}
